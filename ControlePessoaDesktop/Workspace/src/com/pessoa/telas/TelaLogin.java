    private void gravarDtLogin(){
        String sql = "update tb_usuario set ultimoacesso=convert(varchar(10),getdate(),103) where nm_usuario=?";
        try {
            pst = conn.prepareStatement(sql);
            pst.setString(1,txfUsuario.getText());
            pst.executeUpdate();      
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
            
        }
    }
