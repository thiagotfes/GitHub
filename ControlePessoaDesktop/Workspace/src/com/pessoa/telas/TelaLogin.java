    public void Logar() {

        String sql = "select * from tb_usuario where nm_usuario=? and senhas=?";
        try {
            // as linhas abaixo preparam a consulta ao banco em função do que foi digitado nas caixas de texto. 
            // O ? é substituído pelo conteúdo das variáveis.
            pst = conn.prepareStatement(sql);
            pst.setString(1, txfUsuario.getText());
            pst.setString(2, txfSenha.getText());
            // a linha abaixo executa a query
            rs = pst.executeQuery();
            //se existir usuario e senha correspondente
            if (rs.next()) {
                //a linha abaixo obtém o conteúdo do campo perfil da tabela tb_usuario
                String perfil = null;
                perfil = rs.getString(6);
                //System.out.println(perfil);
                //a estrutura abaixo faz o tratamento do perfil do usuário
                if (perfil.equals("admin")) {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.mnArquivo_novoUsuario.setEnabled(true);
                    TelaPrincipal.mnFerramentas_relatorios.setEnabled(true);
                    TelaPrincipal.mnPessoa_novo.setEnabled(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(3));
                    this.dispose();
                    
                } else {
                    TelaPrincipal principal = new TelaPrincipal();
                    principal.setVisible(true);
                    TelaPrincipal.lblUsuario.setText(rs.getString(3));
                    this.dispose();
                    
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuário e/ou senha incorreto(s). Favor Verifique.");
            }
        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

